package com.mybatis.vo;

public class User {

	private long id;
	private Sex sex;
	private String name;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Sex getSex() {
		return sex;
	}

	public void setSex(Sex sex) {
		this.sex = sex;
	}

	@Override
	public String toString() {
		return "User [id=" + id + ", sex=" + sex + ", name=" + name + "]";
	}

}
